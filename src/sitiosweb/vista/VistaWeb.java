    private void btnGuardarwebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarwebActionPerformed
        // TODO add your handling code here:
        ArrayList<sitios> listaSitios = new ArrayList<sitios>();
        listaSitios = controladorSitioweb.obtenersitios();
        for (sitios listaSitio : listaSitios) {
            if (listaSitio.getEnlaceweb().equals(txtEnlaceWeb.getText())) {
                JOptionPane.showMessageDialog(rootPane, "Ya existe un sitio web con este enlace en el sistema.");
                return;
            }
        }
        sitios sitio = new sitios();
        sitio.setNombreweb(txtNombreWeb.getText());
        sitio.setEnlaceweb(txtEnlaceWeb.getText());
        sitio.setFecharegistro(jDateFechaGuardado.getDate());
        if (controladorSitioweb.registrarSitios(sitio)) {
            JOptionPane.showMessageDialog(rootPane, "sitio Web guardado con éxito en el sitema.");
            limpiarsitios();
            modelTablesitioWeb.setsitios(controladorSitioweb.obtenersitios());
            modelTablesitioWeb.fireTableDataChanged();

        } else {
            JOptionPane.showMessageDialog(rootPane, "No se puede guardar el Sitio Web.", "ERROR", JOptionPane.ERROR_MESSAGE);

            if (controladorSitioweb.getEnlace(txtEnlaceWeb.getText()) != null) {
                JOptionPane.showMessageDialog(rootPane, "Ya exisye un sitio web registrado con el este enlace en el sistema.");
            } else {
            }
        }
    }//GEN-LAST:event_btnGuardarwebActionPerformed

