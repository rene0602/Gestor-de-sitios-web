    private void btnEditarwebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarwebActionPerformed
        // TODO add your handling code here:

        if (sitiosEditarEliminar == null) {
            JOptionPane.showMessageDialog(rootPane, "No hay una persona seleccionada para editar", "ERROR", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if (gestionsitios.guardarEditar()) {
            sitiosEditarEliminar.setNombreweb(txtNombreWeb.getText());
            sitiosEditarEliminar.setEnlaceweb(txtEnlaceWeb.getText());
            sitiosEditarEliminar.setFecharegistro(jDateFechaGuardado.getDate());
            if (controladorSitioweb.actualizar(sitiosEditarEliminar)) {
//                JOptionPane.showMessageDialog(rootPane, "Persona editada con exito del sitema.");
                limpiarsitios();
                sitiosEditarEliminar = null;
                actualizarTodaLasitioWeb();
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Un campo es incorrecte", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEditarwebActionPerformed

    private void btnEliminarwebActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarwebActionPerformed
        // TODO add your handling code here:
        if (sitiosEditarEliminar != null) {
            if (controladorSitioweb.eliminar(sitiosEditarEliminar)) {
                JOptionPane.showMessageDialog(rootPane, "Persona eliminada con éxito del sitema.");
                limpiarsitios();
                actualizarTodaLasitioWeb();
                limpiarsitios();
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se puede eliminar la persona seleccionada.", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "No hay persona seleccionada para eliminar.", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEliminarwebActionPerformed

